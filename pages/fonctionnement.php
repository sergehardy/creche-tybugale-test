    <h2 style="text-align: left;">Un accueil familial et convivial</h2>
   <p class="text-justify">La crèche parentale, c’est avant tout une solution alternative entre la crèche et l’assistante maternelle. De la crèche, on garde le côté collectif avec 16 enfants accueillis, et de l’assistante maternelle on garde l’ambiance familiale, un peu «&nbsp;comme à la maison&nbsp;» en veillant à respecter le rythme des enfants.<span style="color: #ffffff;">.</span></p>
    <h3>Un engagement fort</h3>
   <p class="text-justify">La disponibilité des parents est un critère essentiel d’admission de la famille au sein de la crèche.<br>
        Les parents apportent leur contribution pour effectuer des heures de permanences, ainsi que pour les tâches matérielles quotidiennes et de fonctionnement.<span style="color: #ffffff;">.</span></p>
    <h3>16 enfants accueillis</h3>
    <p>De 2 mois ½ à 4 ans<br>
        De manière régulière ou occasionnelle<br>
        De 1 à 5 journées par semaine<br>
        De 8h à 18h30<br>
        Pour des familles résidant à Rennes<span style="color: #ffffff;">.</span></p>
    <h3>Une équipe de parents</h3>
   <p class="text-justify">Des ½ journées de permanences hebdomadaires (4h30 consécutives)<br>
        1 réunion mensuelle (le mercredi de 20h30 à 23h)<br>
        1 poste dans l’association&nbsp;:<br>
        – au sein du bureau (président, trésorier, secrétaire,&nbsp;…)<br>
        – bricolage, moyens généraux, informatique, inscription, communication, …<br>
        1 tour de courses 3 fois par an.<br>
        S’inscrire dans une crèche parentale, c’est être partie prenante de l’éducation des enfants tout en assistant à une période importante de leur vie.<span style="color: #ffffff;">.</span></p>
    <h3>Une équipe pédagogique professionnelle</h3>
    <p>2 Éducateurs de Jeunes Enfants<br>
        2 aides EJE</p>
    <p>Un encadrant pour 4 enfants<br>
        1 cuisinière<span style="color: #ffffff;">.</span></p>
    <h3>Tarifs</h3>
    <p>Cotisation selon les revenus du foyer, comme pour les crèches municipales.<span style="color: #ffffff;">.</span></p>
    <h3>Fermeture annuelle</h3>
    <p>1 semaine entre Noël et le Nouvel An<br>
        3 semaines en août<span style="color: #ffffff;">.</span></p>
    <h3>Accueil d’urgence</h3>
    <p><strong></strong>À la journée ou à la demi-journée<br>
        Pour les enfants de 18 mois à 4 ans<br>
        3 périodes d’accueil envisageables<br>
        En fonction des places disponibles</p>
