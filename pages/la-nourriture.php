<h4>Une place centrale</h4>

<img src="/assets/images/cuisine4.jpg" width="300px" class="float-left"></img>
<p>
    L'alimentation est un élément important dans la vie du nourrisson et du jeune enfant. Elle représente le centre de l'attention pour les parents et les adultes qui interviennent auprès des enfants ainsi qu'une source d'interactions sociales par la communication verbale et non verbale. L'expérience alimentaire apporte non seulement une valeur nutritive mais aussi des occasions d'apprentissages. Elle intervient dans la croissance et la santé physique des enfants ainsi que dans leur développement psycho-social et affectif. La relation alimentaire subit l'influence de la culture, de l'état de santé et des habitudes familiales.</p>

<p>En collectivité, les repas et tout ce qui les entoure est affaire de tous.
</p>

<h4>Au jour le jour</h4>
<p>La nourriture est l’un des piliers de la crèche car c’est un élément essentiel de l’évolution, de la santé et de l’éducation de nos enfants.
</p>

<p>C’est pourquoi Ty Bugale dispose d’un cuisinier qui élabore et prépare les repas sur place pour le plaisir des petits et des grands!</p>

<p>En tant que parent vous pourrez en effet déguster les repas:</p>
<ul>
<li>en cas de permanence le matin, en compagnie des enfants et salariés</li>
    <li>en fin de permanence l’après-midi, en ramenant chez vous des restes.</li>

</ul>

