<h4>Une implication en continu</h4>
<img src="/assets/images/permanence.png" class="float-left"></img>

<p>
Le fonctionnement de la crèche nécessite la présence de parents au quotidien. En plus de l’équipe éducative il y a donc généralement un parent de permanence le matin et deux l’après midi.
</p>

<h4>S’imprégner de la crèche</h4>


<p>Effectuer son temps de permanence permet de voir et de vivre le quotidien de la crèche. Vous avez ainsi la possibilité de voir comment nous fonctionnons, comment votre enfant évolue au sein du groupe. Vous apprenez à connaître l’équipe, les copains copines de votre enfant et leurs parents. Vous pouvez échanger avec votre enfant sur des éléments concrets de sa journée puisque vous connaissez son quotidien.
</p>

<p>
Vivre le quotidien de la crèche permet de comprendre aussi les enjeux de gestion de la structure et ainsi de prendre les meilleures décisions en réunions avec les membres de l’association et les salariés.
</p>

<h4>Contenu d’une permanence</h4>
<img src="/assets/images/metro.png" class="float-left"></img>
<p>
Lors de votre permanence, on vous demandera de nous aider dans les tâches ménagères (lessives, vaisselle, nettoyage, agencements…) mais aussi d’être auprès des enfants une présence rassurante et aidante.
Parfois, nous vous solliciterons pour accompagner une sortie.
</p>
<p>
Une petite “formation permanence” est proposée à chaque rentrée de septembre. C’est l’occasion de poser vos questions, d’entendre parler de la permanence par les parents eux-même.
L’équipe éducative et les autres parents de permanence vous aideront lors de vos premiers pas.
</p>

<h4>Modalités</h4>

<p>
Il est est impératif que les parents organisent leur emploi du temps afin de pouvoir assurer une demi-journée de permanence hebdomadaire.
Le planning des permanences s’effectue d’un mois sur l’autre en fonction des disponibilités et souhaits de chacun.
</p>
