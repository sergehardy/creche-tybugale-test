<h4>Les valeurs</h4>

<h5>Bio</h5>

<p>
    Les repas sont constitués majoritairement d’aliments issus de l’agriculture biologique. La présence d’un cuisinier permet de disposer au quotidien de plats frais, sain et délicieux!
</p>

    <h5>Bienveillance</h5>
<p>
    Les décisions étant prises de manières démocratiques, une communication bienveillante entre les parents, les salariés et les enfants est primordiale afin de respecter la parole et les opinions de chacun.
</p>

