<h4>Le projet éducatif</h4>
<img src="/assets/images/bio.png"  class="float-left">
<p>
Le projet éducatif est un travail de réflexion collective qui permet d’échanger sur les pratiques mises en place à la crèche afin de construire une action pédagogique commune et atteindre des objectifs précis.
</p>
<p>Un projet pédagogique finalise les actions et les valeurs pédagogiques d’une équipe. Il donne du sens et de la cohérence aux actions éducatives pratiquées. C’est un outil pour se recentrer sur l’enfant. Il constitue les fondements de l’analyse des pratiques professionnelles dans le respect des enfants et des familles accueillies.
</p>
<p>C’est une référence commune pour l’équipe à laquelle chacun des membres de l’association adhère. Pour les familles, c’est le moyen de comprendre le travail de l’équipe et de le rendre accessible.
</p>
