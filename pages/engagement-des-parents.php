<h4>Une crèche parentale</h4>

<p>L’implication personnelle des parents au quotidien est une spécificité de la crèche parentale. Si les parents sont avant tout les premiers éducateurs de leurs enfants, ils sont également encadrants, sous la responsabilité de l’éducateur de jeunes enfants ou de l'auxiliaire de puériculture, des autres enfants de la crèche pendant leurs temps de permanence. Chaque parent est donc impliqué, employeur et acteur.</p>

<h4>Une association</h4>

<p>
La crèche Ty Bugale est une association loi 1901. Elle repose avant tout sur l’engagement et l’implication des parents tant au niveau de sa gestion que de son fonctionnement.
</p>
<p>
Chaque famille accueillie régulièrement (plus de 3 jours par semaine)  fait partie du comité directeur. A ce titre, les familles se réunissent une fois par mois lors du Conseil d’Administration pour voter les grandes comme les petites orientations de la crèche. (chaque famille a un droit de vote).
Une fois par an se tient une Assemblée Générale avec toutes les familles accueillies (accueils réguliers, occasionnels…)
</p>

<h4>Les postes</h4>

<p>Chaque famille a un “poste” à pourvoir. Parmi ces postes, cinq postes font partie du bureau de l’association. Le bureau se réunit une fois par mois également, pour préparer le CA.</p>


<h4>Les permanences</h4>

<p>Chaque famille (accueillie 3 jours par semaine minimum à la crèche) effectue une “permanence” de 4h par semaine à la crèche. (en deça de trois jours d’accueil, la sollicitation est proportionnelle au temps d’accueil).</p>

<p>Le bon fonctionnement de l’association est dépendant de la bonne tenue du poste et de la présence active des familles lors de leur permanence.</p>
