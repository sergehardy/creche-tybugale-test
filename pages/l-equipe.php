<h4>Rôle</h4>

<p>L’association de parents emploie des professionnels de la petite enfance pour animer, organiser et accueillir les
    enfants au quotidien.
</p>

<p>Depuis plusieurs années l’équipe de salariés est stable, en contrat à durée indéterminée.
</p>

<p>Elle est composée de :</p>
<ul>
    <li>2 éducateurs de jeunes enfants à temps plein : <strong>Martine et Loïc</strong>.</li>
    <li>1 auxiliaire de puériculture à temps plein : <strong>Marine</strong>.</li>
    <li>2 aides éducatrices diplômées du CAP Petite Enfance à temps plein : <strong>Maly et Nathalie</strong>.</li>
    <li>1 cuisinier à 20h/semaine : <strong>Matthieu</strong>.</li>
</ul>


