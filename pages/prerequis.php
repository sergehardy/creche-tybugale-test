<h4>Règlement</h4>

<h5>Contrat pour les accueils réguliers</h5>

<p>Un contrat est passé avec chaque famille en fonction des besoins d’accueil. Il est préférable que les familles signent une autorisation de prélèvement automatique pour un prélèvement entre le 10 et le 15 de chaque mois et de produire un RIB.</p>
<p>
    En cas de difficulté financière, la famille concernée doit prévenir le trésorier avant la fin du mois précédent pour la cotisation du mois suivant. La crèche fournit les repas et les couches.</p>

<h5>Facturation</h5>

<p>
Le premier jour d'adaptation n'est pas facturé, la facturation prend effet dès le moment où l'enfant reste seul à la crèche. Pendant la semaine d'adaptation, la famille est facturée sur le temps réel passé par l'enfant à la crèche (la contractualisation ne prend effet que la semaine suivante).
</p>
<p>
Pour les entrants et les sortants en cours de mois, la participation familiale est calculée au prorata du temps de présence.</p>
<p>
Les familles s’enregistrent dès l’arrivée et au départ de l’enfant via le logiciel Bel Ami. En cas de dépassement de la plage horaire prévue au contrat, la facturation supplémentaire commencera au-delà de 5 minutes par tranche de 30 minutes suivant le taux horaire de la famille précisé par le contrat d’accueil.</p>

<p>Toute heure réservée au contrat est due par la famille, sauf 4 déductions possibles :</p>

<ul>
    <li>l’éviction de la crèche par le médecin de la crèche</li>
    <li>l’hospitalisation de l’enfant sur présentation d’un bulletin d’hospitalisation</li>
    <li>la fermeture de la crèche</li>
    <li>une maladie, sur présentation d’un certificat médical</li>
</ul>
<h5>Tickets CESU</h5>

<p>
Les familles peuvent régler en tout ou partie leur cotisation au moyen des tickets CESU.
Ils devront en informer le trésorier le plut tôt possible afin qu’ils puissent mettre en place la procédure de remboursement.
</p>






