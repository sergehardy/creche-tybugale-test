<h5>Les ressources prises en compte</h5>

<p>Les ressources prises en compte sont celles communiquées par la CAF (CAFPRO) ou par la MSA.
    L’outil Cafpro est un service de communication électronique proposé par la Caf au gestionnaire qui lui permet via un accès sécurisé de consulter les ressources et le nombre d’enfants à charge des familles. Il est régi par une convention et respecte les règles de confidentialité et a fait l’objet d’un avis favorable de la Cnil.</p>
<p>
Pour les familles non connues dans CAFPRO et ne disposant ni d’avis d’imposition ni de fiche de salaire, sera appliqué le tarif horaire moyen fixe N-2 (total des participations familiales émanant du barème national divisé par les heures facturées au titre de la Psu). Ce tarif sera également appliqué pour les enfants placés à L’ASE et pour les familles refusant d’informer la crèche sur leur numéro d’allocataire ou de fournir leur avis d’imposition.</p>

(cf. document de la CAF « tarification aux familles »)

<h5>Révision des revenus en cours d’année pour un ajustement du barème</h5>

<p>Les familles doivent prévenir le responsable Facturation de tout changement intervenant dans leur revenu ou dans leur situation familiale au cours de l’année afin de pouvoir modifier leur cotisation en cas de :</p>

<ul>
    <li>Chômage : prise en compte après au moins 2 mois consécutifs de chômage total</li>
    <li>Cessation totale d’activité pour s’occuper d’un enfant de moins de 3 ans ou de plusieurs enfants</li>
    <li>Longue maladie</li>
    <li>Changement de situation familiale</li>
    <li>Départ du foyer d’un des deux conjoints / Reprise d’activité / Modification du nombre d’enfants à charge</li>
</ul>
<h5>Année de référence pour la prise en compte des revenus</h5>

<p>
L’année de référence est la même que celle utilisée pour le calcul des prestations familiales soit l’année N-2 pour l‘exercice du 1er janvier au 31 décembre de l’année N.
</p>
<p>
Ainsi les ressources 2018 sont prises en compte du 1er janvier 2020 au 31 décembre 2020.
</p>
<p>
Chaque famille devra donner son numéro d'allocataire à la CAF afin de vérifier que la famille relève du régime général et de pouvoir accéder aux informations communiquées par la CAF (CAFPRO) ou donner son numéro de sécurité sociale (pour accéder aux informations « MSA ») ou fournir l’avis d’imposition N-2.
</p>

<h5>Tarification plancher</h5>
<p>Un forfait minimum de ressources est pris en compte. Ce plancher de ressources est réévalué chaque année par la CAF.</p>

<h5>Tarification plafond</h5>
<p>Les revenus perçus par une famille sont plafonnés. Ce plafond de ressources est réévalué chaque année par la CAF.
Ces tarifications sont modifiées annuellement et applicables au moment de la révision des tarifs aux familles (janvier de chaque année).
</p>
