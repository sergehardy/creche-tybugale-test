<h4>Capacité d’accueil</h4>

<p>La crèche Ty Bugale possède une accréditation de 20 places. La réglementation offre cependant la possibilité de pratiquer l'accueil en surnombre dans la limite de 10 % de
    l’accréditation (article R. 2324-27 du Code de la santé publique).</p>

<ul>
    <li>Accueil régulier : de 2,5 mois à 4 ans</li>
<li>Accueil occasionnel : de ????? à 4 ans</li>
<li>Accueil d'urgence : de 18 mois à 4 ans</li>
</ul>

<h4>Types d’accueil proposés</h4>

<h5>Accueil régulier</h5>

<p>Les besoins sont connus à l’avance et récurrents. Un contrat fondé sur le principe de la mensualisation est établi sur un volume d’heures déterminé avec les parents selon leurs besoins.
</p>

<h5>Accueil en surnombre</h5>

<p>Celui-ci ne peut être pratiqué que certains jours de la semaine et à condition que la moyenne hebdomadaire du taux d’occupation n’excède pas 100% de la capacité d’accueil prévue par l’autorisation du Président du Conseil départemental.
</p>

<h5>Accueil occasionnel</h5>

<p>Pour des enfants de plus de 18 mois : les places restées vacantes par les enfants en accueil régulier (vacances, maladies...) peuvent être proposées à des enfants connus de la crèche et accueillis ponctuellement.
</p>

<h5>Accueil d'urgence</h5>

<p>pour des enfants non inscrits préalablement dans la mesure des places disponibles qui permet de maintenir un taux d'encadrement adéquat.
</p>

<h4>Accueil d’urgence</h4>

<p>Les conditions permettant l’accueil d’urgence à la crèche Ty Bugale sont les suivantes:</p>

<ul>
    <li>Une place est disponible</li>
<li>Le taux d’encadrement est adéquat</li>
<li>L'enfant a entre 18 mois et 4 ans</li>
<li>Contexte : l'enfant subit une rupture brutale et inattendue de son mode de garde habituel</li>
</ul>

<p>
Ce type d’accueil est renouvelable 3 fois par enfant sur l’année. Le règlement de la crèche ne permet pas d’arrivée entre 11h et 13h.
</p>
<p>Le coût minimal pour cet accueil dépend du montant plancher calculé par la trésorerie à la demi-journée que la crèche communiquera à l’Étoile.</p>

<p>A noter:</p>
<ul>
    </li>
    <li>Il n'y a pas d'accueil d'urgence pendant une période d'adaptation</li>
    <li>Contacter la crèche pour une place d'urgence et non le poste inscription</li>
    <li>La décision d'accueillir ou non en urgence est prise par l'équipe éducative et les responsables techniques au regard de tous ces points de condition</li>
</ul>

