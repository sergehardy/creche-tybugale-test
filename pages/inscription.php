<h4>Modalités d'inscription</h4>

<p>La crèche parentale Ty Bugale accueille tout enfant de 2,5 mois à 4 ans sans discrimination (âge, sexe, religion, handicap...) en essayant de maintenir les équilibres nécessaires au bon fonctionnement de la crèche selon les critères suivants :</p>

<ul>
    <li>ancienneté de la demande d’inscription sur la liste d’attente</li>
    <li>âge des enfants</li>
    <li>nombre de jours demandé et nombre d'heures souhaitées</li>
<li>adhésion au projet de la crèche (disponibilité pour les permanences, participation aux C.A., commissions ou réunions, investissement dans son poste…)
    </li>
</ul>
<p>
Aucune condition d’activité professionnelle ou assimilée des deux parents ou du parent unique n’est exigée.
</p>
<p>
    Les enfants issus de la famille d’un salarié de la crèche ne sont pas admis.
</p>
<p>Seuls les enfants rennais sont accueillis à la crèche. En cas de déménagement dans une autre commune en cours de contrat, l'enfant peut éventuellement continuer à être accueilli dans la crèche Ty Bugale. Dans ce dernier cas, afin de maintenir l’inscription, un complément financier devra être négocié avec la nouvelle commune d’origine, dans la mesure où la crèche ne bénéficiera plus de subventions de la ville de Rennes pour cet enfant. A défaut, l’accueil ne pourra plus être garanti.
</p>
<p>
Une famille souhaitant inscrire son enfant à la crèche est reçue par le(s) parent(s) responsable(s) des inscriptions et un membre de l’équipe pédagogique, qui se chargent de lui expliquer les modalités d’inscription et de fonctionnement de la crèche, les engagements que cela implique, le projet associatif et éducatif de la crèche, et de lui faire visiter le lieu d’accueil.
</p>

<h4>Procédure d’admission</h4>

<p>Qui prend la décision ?</p>

<p>
La décision d’admission d’une nouvelle famille à la crèche est prise par le bureau, après réception de l’avis des parents chargés des inscriptions et de celui du responsable technique.</p>
<p>
En cas de refus, chaque famille dispose d’un délai de deux mois pour exercer un recours gracieux auprès du (de la) Président(e), puis de 2 mois - en cas de réponse négative - auprès du Tribunal administratif de Rennes.
</p>
<p>
Un courrier ou un mail avec accusé réception est envoyé à chaque famille reçue en entretien, pour les informer de leur admission ou du refus.
</p>
<p>Sont prioritaires à condition de répondre aux critères d'âge, de nombre de jours et heures demandées
au contrat, disponibilité et adhésion des parents au projet :</p>
<ul>
<li>les parents qui ont déjà un enfant à la crèche</li>
    <li>un enfant présent en accueil partiel (1 à 3 journées par semaine) dont les parents désirent augmenter le temps d'accueil</li>
    <li>un enfant accueilli occasionnellement qui souhaiterait une place régulière</li>
</ul>
<h4>Inscription définitive</h4>


<p>
Le contrat d’accueil est signé pour une période d’1 an maximum (il peut aussi couvrir une période inférieure), du 1er septembre au 31 juillet et peut être révisé à la demande des deux parties en cas de contrat inadapté aux heures de présence de l’enfant.</p>

<p>Le contrat d’accueil est adapté aux besoins des parents et détermine le nombre d’heures de présence de l’enfant par jour, de jour par semaine et de semaines par an, en tenant compte de toutes les absences prévisibles de l’enfant demandées par la famille.
</p>
<p></
Une fois l’admission de leur enfant validée, le(s) parent(s) s’engage(nt) à fournir au parent responsable des inscriptions :</p>
<ul>
    <li>le règlement de fonctionnement lu, accepté et signé</li>
    <li>l’adresse et le(s) numéro(s) de téléphone des parents, ainsi que leur adresse électronique</li>
    <li>le contrat d’accueil, lu, approuvé et signé</li>
    <li><li>la fiche sanitaire de l’enfant avec la photocopie du carnet de vaccination</li>
    <li>la fiche administrative, complétée et signée, comprenant
    <li>le numéro d’allocataire de la CAF, de la MSA ou tout autre régime, et l'autorisation de l'utiliser pour accéder aux informations nécessaires au calcul du tarif horaire
    <li>l’autorisation de sortie avec un tiers avec les coordonnées des personnes habilitées</li>
    <li>l'autorisation de prélèvement datée et signée accompagnée d'un RIB (si paiement par virement)</li>
    <li>l’autorisation d’hospitalisation</li>
    <li>l'autorisation de photographier et filmer l'enfant</li>
    <li>l'autorisation d'utiliser les films et photos de l'enfant pour la communication de la crèche</li>
    <li>l’autorisation de sortie à l’extérieur des locaux de la crèche</li>
    <li>la photocopie du carnet de vaccination pour les parents</li></li>
    <li>l’attestation d’assurance à jour du véhicule susceptible d’être utilisé pour les sorties
    <li>en cas d'allergies : un justificatif médical précisant l'allergie et la conduite à tenir (voire nécessitant la mise en place d'un Protocole d'Accueil Individualisé</li>
</ul>

<p>Pour les accueils d’urgence, une procédure plus rapide d’admission pourra être mise en place.
    Seront alors demandés uniquement :</p>
<ul>
<li>le contrat d’accueil, lu, approuvé et signé
    <li>le numéro d’allocataire de la CAF</li>
    <li>l’adresse et le(s) numéro(s) de téléphone des parents ainsi que leur adresse électronique</li>
    <li>la fiche sanitaire de l’enfant</li>
    <li>l’autorisation d’hospitalisation</li>
    <li>la photocopie du carnet de vaccination des enfants</li>
    <li>la fiche des repas et des rythmes de journée</li>
</ul>

<p>Si la famille refuse de fournir son numéro d’allocataire CAF, quelque soit le type d’accueil, elle s’engage à fournir l’avis d’imposition N-2. A défaut, elle sera facturée au prix plafond proposé par la CAF.
</p>
<p>
    Une période d’essai d’un mois permet à chacune des deux parties de revenir sur sa décision. Au terme de cette période, le responsable technique procède à un entretien avec les parents.</p>
<p>
    Après le mois d’essai, l’admission est définitive et ne prend fin que dans l’un des cas suivants :
</p>
<ul>
    <li>l’enfant a atteint 4 ans, l’âge limite pour être admis à la crèche</li>
    <li>les parents manifestent leur intention de ne plus confier leur enfant à la crèche, en donnant un préavis de trois mois avant son départ effectif (ramené à un mois en cas de déménagement ou pour raison professionnelle). Ce préavis se fait par écrit et doit être adressé au (à la) Président(e) de l’association. Ce(tte) dernier(e) informe le parent chargé des inscriptions et le(a) trésorier(e) du départ.</li>
    <li>la radiation des parents a été prononcée par l’association : en effet, cette dernière se réserve le droit d’exclure un de ses membres par décision prise en assemblée générale, pour motifs graves (non respect du règlement de fonctionnement, attitude dangereuse, portant préjudice aux familles ou perturbant le bon fonctionnement de la crèche).</li>
</ul>

<p>Si le besoin d’accueil est modifié entre la demande de pré-inscription et la signature du contrat d’accueil, le(a) Président(e) de l’association pourra le cas échéant re-considérer la place proposée comme vacante.
</p>